package com.fitbit.model;

public class Stages {
    public int deep;
    public int light;
    public int rem;
    public int wake;

    public int getDeep() {
        return deep;
    }

    public void setDeep(int deep) {
        this.deep = deep;
    }

    public int getLight() {
        return light;
    }

    public void setLight(int light) {
        this.light = light;
    }

    public int getRem() {
        return rem;
    }

    public void setRem(int rem) {
        this.rem = rem;
    }

    public int getWake() {
        return wake;
    }

    public void setWake(int wake) {
        this.wake = wake;
    }
}
